/*
    * String é o tipo de dados para texto.
    
    * para representarmos uma string temos que coloca-la entre 
    aspas simples, aspas duplas, ou crazes (aspas simples deitadas).

    * Um número entre aspas é considerado como String.
*/

var nome = "Daniel"
var sobrenome = "Pinheiro"

console.log(`"${nome}": ${typeof nome}`)
console.log(`"4.24": ${typeof "4.24"}`)

console.log(nome + " " + sobrenome)

console.log("Olá, meu nome é 'Daniel'")

console.log(`Olá, meu nome é "João"`)
